function IDSelectHandler() {
			var selectID = document.getElementsByName("inputSelectID")[0];
			var inputID = document.getElementsByName("inputID")[0];
			inputID.value = selectID.value;
		}

function pickupOptionHandler(event) {
			// Query the divs only once
			var noOption = document.getElementById("noOption");
			var scw = document.getElementById("scw");
			var mail = document.getElementById("mail");
			var authorized = document.getElementById("authorized");

			// Switch on the selected option
			switch (event.target.value) {
				case "Pickup at SCW":
					noOption.style.display = "none";
					scw.style.display = "block";
					mail.style.display = "none";
					$('#mail').hide();
					$('.resp-req').attr('required', false);
					authorized.style.display = "none";
					$('#authorized').hide();
					$('.resp-req1').attr('required', false);	
					break;
				case "Mail to Address":
					noOption.style.display = "none";
					scw.style.display = "none";
					mail.style.display = "block";
					$('#mail').show();
					$('.resp-req').attr('required', true);
					authorized.style.display = "none";
					$('#authorized').hide();
					$('.resp-req1').attr('required', false);	
					break;
				case "Authorized Person Pickup":
					noOption.style.display = "none";
					scw.style.display = "none";
					mail.style.display = "none";
					$('#mail').hide();
					$('.resp-req').attr('required', false);
					authorized.style.display = "block";
					$('#authorized').show();
					$('.resp-req1').attr('required', true);	
					IDSelectHandler();
					break;
				default:
					noOption.style.display = "block";
					scw.style.display = "none";
					mail.style.display = "none";
					authorized.style.display = "none";
			}
		}
</script>
